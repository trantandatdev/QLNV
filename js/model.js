function Employee(
  idEmployee,
  nameEmployee,
  emailEmployee,
  passEmployee,
  datepickerEmployee,
  salaryEmployee,
  positionEmployee,
  timeEmployee,
  totalEmployee,
  categoryEmployee
) {
  this.id = idEmployee;
  this.name = nameEmployee;
  this.email = emailEmployee;
  this.pass = passEmployee;
  this.datepicker = datepickerEmployee;
  this.salary = salaryEmployee;
  this.position = positionEmployee;
  this.time = timeEmployee;
  this.total = totalEmployee;
  this.category = categoryEmployee;
}
